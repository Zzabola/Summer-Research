    wire [127:0] aes_din = {slv_reg11, slv_reg10, slv_reg9, slv_reg8};
    
    // pulse signals derived from CTRL write (slv_reg0 self-clears)
    wire pulse_set_key = slv_reg0[0];
    wire pulse_encrypt = slv_reg0[1];
    wire pulse_decrypt = slv_reg0[2];
    
    // AES status signals
    wire status_key_done;
    wire status_enc_done;
    wire status_dec_done;
    wire status_enc_busy;
    wire status_dec_busy;
    wire set_key_enable_unused;
    
    wire [127:0] aes_plaintext_out;
    wire [127:0] aes_ciphertext_out;
    
    AES u_aes_core (
      .clk              (S_AXI_ACLK),
      .rst_n            (rst_n),
      .set_key_i        (pulse_set_key),
      .key_i            (aes_key),
      .set_key_enable_o (set_key_enable_unused),
      .gen_key_done_o   (status_key_done),
      .encrypt_i        (pulse_encrypt),
      .plaintext_i      (aes_din),
      .encrypt_busy_o   (status_enc_busy),
      .encrypt_done_o   (status_enc_done),
      .ciphertext_o     (aes_ciphertext_out),
      .decrypt_i        (pulse_decrypt),
      .ciphertext_i     (aes_din),
      .decrypt_busy_o   (status_dec_busy),
      .decrypt_done_o   (status_dec_done),
      .plaintext_o      (aes_plaintext_out)
    );
    
    // STATUS register: update continuously (software reads slv_reg1)
    always @(posedge S_AXI_ACLK) begin
      if (!rst_n) begin
        slv_reg1 <= 32'b0;
      end else begin
        // {zeros, dec_busy, enc_busy, dec_done, enc_done, key_done}
        slv_reg1 <= {27'b0, status_dec_busy, status_enc_busy,
                     status_dec_done, status_enc_done, status_key_done};
      end
    end
    
    // Latch outputs into DOUT regs on done
    always @(posedge S_AXI_ACLK) begin
      if (!rst_n) begin
        slv_reg12 <= 32'b0;
        slv_reg13 <= 32'b0;
        slv_reg14 <= 32'b0;
        slv_reg15 <= 32'b0;
      end else begin
        if (status_enc_done) begin
          slv_reg12 <= aes_ciphertext_out[31:0];
          slv_reg13 <= aes_ciphertext_out[63:32];
          slv_reg14 <= aes_ciphertext_out[95:64];
          slv_reg15 <= aes_ciphertext_out[127:96];
        end else if (status_dec_done) begin
          slv_reg12 <= aes_plaintext_out[31:0];
          slv_reg13 <= aes_plaintext_out[63:32];
          slv_reg14 <= aes_plaintext_out[95:64];
          slv_reg15 <= aes_plaintext_out[127:96];
        end
      end
    end
	// User logic ends

	endmodule
